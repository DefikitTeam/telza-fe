export const checkBannedWords = (_str: string) => {
  const str = _str || ''

  const normalizedStr = str.toLowerCase().trim()
  const bannedWordsLowerCase = bannedWords.map((word) => word.toLowerCase())
  // Function to check if a word is surrounded by word boundaries
  const isWholeWord = (str: string, word: string) => {
    const regex = new RegExp(`\\b${word}\\b`, 'i')
    return regex.test(str)
  }
  // Function to find all banned words in the string
  const findBannedWords = (str: string): string[] => {
    return bannedWordsLowerCase.filter((word) => isWholeWord(str, word))
  }

  // Find all banned words in the normalized string
  const foundBannedWords = findBannedWords(normalizedStr)

  // If banned words are found, return them as an array
  // Otherwise, return false
  if (foundBannedWords.length > 0) {
    return Array.from(new Set(foundBannedWords))
  }

  // Check if any banned word is present as a whole word
  return bannedWordsLowerCase.some((word) => isWholeWord(normalizedStr, word))
}
